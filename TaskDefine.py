class Task:
    def __init__(self, process_list):
        self.processes = process_list
        behaviour_sequence = [[] for _ in range(2000)]
        for i,p in enumerate(process_list):
            behaviour_sequence[i].append(({'process':p, 'request':p.block}))
            behaviour_sequence[i+p.duration].append(({'process':p, 'request':'free'}))
        self.process_behaviour = sum(behaviour_sequence, [])
        pass

    def __next__(self):
        if self.current == len(self.process_behaviour):
            raise StopIteration()
        behaviour = self.process_behaviour[self.current]
        self.current += 1
        return behaviour
    
    def __iter__(self):
        self.current = 0
        return self


